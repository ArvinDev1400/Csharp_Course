int a = 10;
int b = 20;

// equal => ==
if (a == b)
{
    Console.WriteLine("a مساوی با b است");
}
else
{
    Console.WriteLine("a مساوی با b نیست");
}

// not equal => !=
if (a != b)
{
    Console.WriteLine("a برابر با b نیست");
}

// greater than => >
if (b > a)
{
    Console.WriteLine("b بزرگتر از a است");
}

// less than => <
if (a < b)
{
    Console.WriteLine("a کوچکتر از b است");
}

// Greater than or equal => >=
if (b >= 20)
{
    Console.WriteLine("b بزرگتر یا مساوی 20 است");
}

// Less Than or equal => <=
if (a <= 10)
{
    Console.WriteLine("a کوچکتر یا مساوی 10 است");
}